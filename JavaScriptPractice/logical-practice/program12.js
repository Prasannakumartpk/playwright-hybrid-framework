
let a = 5;

console.log(a++); 

console.log(++a);

/* let a = 5;
→ Variable a is initialized to 5.

console.log(a++); (Post-increment)

Post-increment: return the current value, then increment.

Prints 5.

After this line, a becomes 6.

console.log(++a); (Pre-increment)

Pre-increment: increment first, then return.

a was 6, increment → 7.

Prints 7.*/